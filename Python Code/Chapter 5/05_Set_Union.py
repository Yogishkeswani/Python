s1 = {1 ,45 ,6}

s2 = {7 , 8, 1, 78 }

print("The union of the set is : ", s1.union(s2)) # The union of sets is the process of combining all the unique elements from two or more sets into a single new set, without any repetitions.

print("The intersection of the set is : ", s1.intersection(s2)) # The intersection of sets is a fundamental operation in set theory that identifies the elements common to two or more sets