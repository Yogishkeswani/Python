# Syntax - 
# <variable> =[elemnet 1 , element 2 , element 3,..., element n]

# print (<variable>[index])

# Example
friends = ["Apple","Orange",5,45.2,False,"Akash", "Rohan"]

print(friends[0]) # It will print the element of index 0 

friends[0] = "Grapes" # Unlike Strings, lists are mutable

print(friends[0])   

print(friends[1:4])

print(friends[-4:-2])
print(friends[2])