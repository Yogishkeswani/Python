# range(4) means:
# Start from 0 by default
# Go till 3 (4 is excluded)
# Increase by 1 each time by default

for i in range(4):
    print(i)

# Output:
# 0
# 1
# 2
# 3








# range(0, 100, 4) means:
# Start from 0
# Go till 99 (100 is excluded)
# Increase the value by 4 each time

for i in range(0, 100, 4):
    print(i)

# Output:
# 0
# 4
# 8
# 12
# ...
# 96